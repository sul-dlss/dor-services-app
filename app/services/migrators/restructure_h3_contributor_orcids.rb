    def migrate # rubocop:disable Metrics/AbcSize, Metrics/CyclomaticComplexity, Metrics/PerceivedComplexity
      repository_object.versions.each do |version|
        next unless version.description

        version.description['contributor'].each do |contributor|
          Array(contributor['identifier']).each do |contributor_identifier|
            next unless contributor_identifier['type'] == 'ORCID' &&
                        contributor_identifier.dig('source', 'uri')&.match?(/orcid.org/) &&
                        contributor_identifier['value'].present?

            contributor_identifier['uri'] = "https://orcid.org/#{contributor_identifier.delete('value')}"
            contributor_identifier.delete('source')
          end
        end
